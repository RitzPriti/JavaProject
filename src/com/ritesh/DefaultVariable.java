package com.ritesh;

public class DefaultVariable {
    static boolean a;
    static char b;
    static int e;
    static long f;
    static float g;
    static double h;

    public static void main(String[] args) {

        System.out.println(a);
        System.out.println(b);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);
    }

}
